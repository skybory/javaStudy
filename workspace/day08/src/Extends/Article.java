package Extends;

public class Article {
	private int num;
	private String title;
	
	//Article()	{}	 안이 비어있는 기본 생성자는 입력하지 않아도 있는 것으로 간주함.
	
	//getter 와 setter 설정
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	
	

}
